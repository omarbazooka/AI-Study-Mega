# Mock confidence level as requested (not high confidence for mock outputs)
MOCK_CONFIDENCE = 0.5

def check_no_answer_trigger(query: str) -> bool:
    """Checks if query simulates requesting information outside the document context."""
    normalized = query.lower()
    return "خارج الملف" in normalized or "outside the file" in normalized

async def run_chat_answer_pipeline(task: Task, request: Any, previous_results: Optional[Dict[str, Any]] = None) -> TaskResult:
    """Simulates localized QA search pipeline using vector chunks (top-k)."""
    if check_no_answer_trigger(task.query):
        return TaskResult(
            task_id=task.task_id,
            type=TASK_CHAT_ANSWER,
            status="no_answer",
            content=NO_ANSWER_FALLBACK,
            citations=[],
            confidence=0.0,
            metadata={"mock": True}
        )

    lang = getattr(request, "language", "ar")
    if lang == "ar":
        content = f"هذا جواب محاكاة لسؤالك: '{task.query}' استناداً إلى محتوى الملف."
    else:
        content = f"This is a simulated answer to your question: '{task.query}' grounded strictly in the document."

    return TaskResult(
        task_id=task.task_id,
        type=TASK_CHAT_ANSWER,
        status="success",
        content=content,
        citations=[Citation(chunk_id="chunk_chat_1", page_number=1, score=0.85)],
        confidence=MOCK_CONFIDENCE,
        metadata={"mock": True, "pipeline": "vector_chat_top_k"}
    )

