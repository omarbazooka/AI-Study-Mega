async def run_answer_table_pipeline(task: Task, request: Any, previous_results: Optional[Dict[str, Any]] = None) -> TaskResult:
    """Simulates generating an answer table. Consumes outputs from dependent quiz task if sequential execution."""
    if check_no_answer_trigger(task.query):
        return TaskResult(
            task_id=task.task_id,
            type=TASK_ANSWER_TABLE,
            status="no_answer",
            content=NO_ANSWER_FALLBACK,
            citations=[],
            confidence=0.0,
            metadata={"mock": True}
        )

    lang = getattr(request, "language", "ar")
    
    # Retrieve questions generated by previous task if it was sequential
    questions = []
    if previous_results:
        for t_res in previous_results.values():
            if t_res.type == TASK_QUIZ and "generated_questions" in t_res.metadata:
                questions = t_res.metadata["generated_questions"]
                break

    # Build Markdown table based on input questions
    if questions:
        headers = ["السؤال", "الإجابة الصحيحة"] if lang == "ar" else ["Question", "Correct Answer"]
        rows = [f"| {q['question']} | {q['correct']} |" for q in questions]
        content = f"### {headers[0]} / {headers[1]}\n| {headers[0]} | {headers[1]} |\n|---|---|\n" + "\n".join(rows)
    else:
        # Fallback if no sequential quiz input was passed
        if lang == "ar":
            content = "### جدول الإجابات النموذجية\n| السؤال | الإجابة الصحيحة |\n|---|---|\n| السؤال 1 | الخيار الأول |\n| السؤال 2 | الخيار الثاني |"
        else:
            content = "### Answers Table\n| Question | Correct Answer |\n|---|---|\n| Question 1 | Option A |\n| Question 2 | Option B |"

    return TaskResult(
        task_id=task.task_id,
        type=TASK_ANSWER_TABLE,
        status="success",
        content=content,
        citations=[Citation(chunk_id="chunk_answer_table_ref", page_number=2, score=0.90)],
        confidence=MOCK_CONFIDENCE,
        metadata={"mock": True, "consumed_quiz_questions": len(questions) > 0}
    )

